// Variables are - var,let,const
// SYNTAX FOR DECLARING VARIABLE
// Datatype varname= value 

var age=25;
console.log(age);
var age=35;
console.log(age);

// Naming convention for declaring variables

// firstname
// first_name
// FirstName used in Classes
// firstName(pref) Camel casing

// VAR IS GLOBAL(SCOPE)
// LET AND CONST --> LOCAL SCOPE
//LET AND CONST CAN BE ACCESSED ONLY INSIDE THE BLOCK.
//Let value can be changed without declaring the variable name(IT CAN BE REASSIGNED).
//Const value cannot be  changed once it is declared.(IT CANNOT BE REASSIGNED).

let k=30;
k=60;
console.log(k);

const a=19;
console.log(a);
